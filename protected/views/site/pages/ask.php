	<h1>Preguntas frecuentes</h1>
	<p class="text-info">
	
		<strong>&iquest;Qu&eacute; es el Impuesto a la Transferencia Inmueble (I.T.I.)?</strong><br/>
		La venta de inmuebles se grava con una tasa del 1,5% sobre el valor total de escrituraci&oacute;n
		y ser&aacute; retenido por el escribano al vendedor de la propiedad. Sin embargo, se exime de
		este impuesto al vendedor que transfiere su vivienda para adquirir otra dentro del plazo de
		un a&ntilde;o, a&uacute;n cuando la compra del bien de reemplazo hubiera sido anterior.<br/><br/>

		<strong>&iquest;Qu&eacute; es el C&oacute;digo de Oferta de Transferencia de Inmuebles (C.O.T.I.)?</strong><br/>
		El COTI es el "c&oacute;digo de oferta de transferencia de inmuebles", el cual debe ser obtenido
		por el titular o cond&oacute;mino de bienes inmuebles con car&aacute;cter previo a la negociaci&oacute;n, oferta
		o transferencia de un bien inmueble o de derechos sobre bienes inmuebles a construir.<br/>
		A partir del 01/03/2008 el titular de un inmueble deber&aacute; obtener el "C&oacute;digo de oferta de
		transferencia de inmuebles" (COTI), con car&aacute;cter previo a la publicaci&oacute;n del inmueble,
		siempre que el precio de comercializaci&oacute;n resulte igual o superior a $600.000.- Se podr&aacute;
		informar a la AFIP a trav&eacute;s del sitio <a href="http://www.afip.gov.ar" target="_blank">www.afip.gov.ar</a><br/><br/>

		<strong>&iquest;Cu&aacute;les son los plazos para alquilar?</strong><br/>
		La Ley 23.091 de Locaciones Urbanas, en su art. 2do., establece que el m&iacute;nimo para
		locaciones de viviendas es de 2 a&ntilde;os; para el caso de contratos comerciales el m&iacute;nimo es
		de 3 a&ntilde;os.<br/><br/>

		<strong>&iquest;Puede el inquilino irse antes de tiempo?</strong><br/>
		Resoluci&oacute;n anticipada. El locatario podr&aacute;, transcurridos los seis primeros meses de
		vigencia de la relaci&oacute;n locativa, resolver la contrataci&oacute;n, debiendo notificar en forma
		fehaciente su decisi&oacute;n al locador con una antelaci&oacute;n m&iacute;nima de sesenta d&iacute;as de la fecha
		en que reintegrar&aacute; lo arrendado.<br/>
		El locatario, de hacer uso de la opci&oacute;n resolutoria en
		el primer a&ntilde;o de vigencia de la relaci&oacute;n locativa, deber&aacute; abonar al locador, en concepto
		de indemnizaci&oacute;n, la suma equivalente a un mes y medio de alquiler al momento de
		desocupar la vivienda y la de un solo mes si la opci&oacute;n se ejercita transcurrido dicho
		lapso. La solicitud de resoluci&oacute;n anticipada por parte del inquilino no eximir&aacute; al mismo
		del cumplimiento de las obligaciones contractuales consagradas a su exclusivo cargo, en
		particular las referidas a la forma y condiciones que deber&aacute; tener el bien locado a la fecha
		de entrega de llaves.<br/><br/>

		<strong>&iquest;Qu&eacute; documentaci&oacute;n necesito para poner a la venta mi casa?</strong><br/>

		Se necesita la Escritura Traslativa de Dominio, el Documento Nacional de Identidad de los titulares
		y la Autorizaci&oacute;n de venta.<br/><br/>
		
		<strong>&iquest;Qu&eacute; es la Autorizaci&oacute;n de venta?</strong><br/>

		Es autorizar al agente inmobiliario a vender mi casa, ya que sin este requisito, el corredor no
		puede vender propiedades ajenas. La Ley 10973 Art. 52 Inc. 9 dice: "Convenir por escrito con sus
		mandantes los honorarios y gastos, las condiciones de venta, la forma de pago de todo cuanto
		creyera conveniente para el mejor desempe&ntilde;o de su mandato".<br/>
		La autorizaci&oacute;n es imprescindible
		para que el profesional inmobiliario pueda pedir informes de Dominio e Inhibici&oacute;n ante el Registro
		de la Propiedad Inmueble (Ley 10.973, Art. 52 y 53) colocar carteler&iacute;a, tomar fotograf&iacute;as, realizar
		las publicaciones correspondientes, visitas, guardias, recibir ofertas por la propiedad, redactar el
		Boleto de Compraventa.<br/><br/>

		<strong>&iquest;Qu&eacute; es la Reserva de Compra?</strong><br/>

		Este documento simplemente es una "oferta" que el oferente realiza por la propiedad en
		cuesti&oacute;n depositando una suma de dinero. En todos los casos dicha reserva est&aacute; condicionada o
		ad referendum del propietario del inmueble y ser&aacute; devuelta en forma inmediata si el mismo no
		aceptara lo ofrecido por el comprador.<br/><br/>

		<strong>&iquest;Para qu&eacute; sirve la reserva?</strong><br/>

		Abre una instancia de negociaci&oacute;n seria entre las partes. En algunos casos se puede realizar
		una se&ntilde;a una vez conformada la oferta, incluyendo un pacto comisorio en la misma.<br/>
		Independientemente de eso, una vez que la propiedad es reservada, el inmueble es retirado de la
		venta y se comienzan los tr&aacute;mites preparatorios para la suscripci&oacute;n del Boleto de Compraventa o
		de la Escritura directa (siempre posterior a la aceptaci&oacute;n).<br/><br/>

		<strong>&iquest;Qu&eacute; debe figurar principalmente en la reserva?</strong><br/>

		Debe constar el precio que el oferente/comprador desea pagar por el inmueble, el plazo
		para la realizaci&oacute;n del Boleto de Compraventa o de la Escritura, qui&eacute;n va a ser el Escribano
		interviniente, c&oacute;mo ser&aacute;n soportados los gastos de escritura y cuanto le va a cobrar de honorarios
		la inmobiliaria.<br/><br/>

		<strong>Ya aprobada la oferta por el propietario y comunicado ello al Comprador. &iquest;C&oacute;mo siguen
		los tr&aacute;mites?</strong><br/>
		Normalmente con la firma del Boleto de Compraventa, dentro del plazo de los d&iacute;as que figuran en
		la misma reserva.<br/><br/>

		<strong>&iquest;Qu&eacute; porcentaje del valor de la propiedad se paga normalmente en el Boleto de
		Compraventa?</strong><br/>
		La parte compradora paga a cuenta de precio el 30% del total de la compraventa. No obstante,
		este porcentaje puede ser distinto, de com&uacute;n acuerdo entre las partes.
